# Function to replace names-----------------------------------------------------
fix_player_names_pointsbet <- function(player_names, lookup_table) {
    # Create a vector to store the fixed names
    fixed_names <- character(length(player_names))
    
    # Iterate through each player name
    for (i in seq_along(player_names)) {
        # Check if the name exists in the lookup table
        if (player_names[i] %in% names(lookup_table)) {
            # If it does, replace with the correct name
            fixed_names[i] <- lookup_table[player_names[i]]
        } else {
            # If it doesn't, keep the original name
            fixed_names[i] <- player_names[i]
        }
    }
    
    return(fixed_names)
}
